vi bit(MX,0);

int get(int i){
	int ans=0;
	while(i){
		ans+=bit[i];
		i-=i&-i;
	}
	return ans;
}

void update(int i,int v){
	while(i<MX){
		bit[i]+=v;
		i+=i&-i;
	}
}


